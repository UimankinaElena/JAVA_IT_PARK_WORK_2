public class Task21{
	public static void main(String args[]) {
		
		char text[] = {'3','2','3','1', '!', '?'};
				
		for (int i = 0; i < text.length; i++) {
			 
			if (text[i] == ';')System.out.print(text[i]);
			if (text[i] == ',')System.out.print(text[i]);
			if (text[i] == ':')System.out.print(text[i]);
			if (text[i] == '?')System.out.print(text[i]);
			if (text[i] == '-')System.out.print(text[i]);
			if (text[i] == '"')System.out.print(text[i]);
			if (text[i] == '!')System.out.print(text[i]);
			
			}				
				
	}

}

	
		